DOCX = Path("KVR_Burunduk_Garage_full_report.docx")


def run_font(run, size=14, bold=False):
    run.font.name = "Times New Roman"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    run.font.size = Pt(size)
    run.bold = bold


def format_like_body(paragraph):
    paragraph.style = paragraph.part.document.styles["Normal"]
    paragraph.paragraph_format.first_line_indent = Cm(1.25)
    paragraph.paragraph_format.left_indent = Cm(0)
    paragraph.paragraph_format.line_spacing = 1.5
    paragraph.paragraph_format.space_before = Pt(0)
    paragraph.paragraph_format.space_after = Pt(0)
    paragraph.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    for run in paragraph.runs:
        run_font(run)


def insert_after(paragraph, text):
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_paragraph = Paragraph(new_p, paragraph._parent)
    new_paragraph.add_run(text)
    format_like_body(new_paragraph)
    return new_paragraph


def set_text(paragraph, text):
    paragraph.text = text
    format_like_body(paragraph)


def first_heading_index(doc, prefix):
    return next(
        i for i, p in enumerate(doc.paragraphs)
        if p.style.name.startswith("Heading") and p.text.strip().startswith(prefix)
    )


def heading_index(doc, exact):
    return next(
        i for i, p in enumerate(doc.paragraphs)
        if p.style.name.startswith("Heading") and p.text.strip() == exact
    )


def next_heading_index(doc, start):
    for i in range(start + 1, len(doc.paragraphs)):
        p = doc.paragraphs[i]
        if p.style.name.startswith("Heading") and p.text.strip():
            return i
    return len(doc.paragraphs)


def anchor_for_heading(doc, exact):
    start = heading_index(doc, exact)
    end = next_heading_index(doc, start)
    idx = end - 1
    while idx > start and not doc.paragraphs[idx].text.strip():
        idx -= 1
    return doc.paragraphs[idx]


def replace_contains_before(doc, idx4, needle, new_text):
    for p in doc.paragraphs[:idx4]:
        if needle in p.text:
            set_text(p, new_text)
            return True
    return False


def replace_text_before(doc, idx4, replacements):
    for p in doc.paragraphs[:idx4]:
        text = p.text
        changed = False
        for old, new in replacements:
            if old in text:
                text = text.replace(old, new)
                changed = True
        if changed:
            set_text(p, text)


def add_after_heading(doc, heading, paragraphs):
    anchor = anchor_for_heading(doc, heading)
    for text in paragraphs:
        anchor = insert_after(anchor, text)



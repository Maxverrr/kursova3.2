def clear_doc(doc: Document) -> None:
    body = doc._body._element
    for child in list(body):
        if child.tag != qn("w:sectPr"):
            body.remove(child)


def setup_section(section) -> None:
    section.page_height = Cm(29.7)
    section.page_width = Cm(21)
    section.top_margin = Cm(1.5)
    section.bottom_margin = Cm(3.0)
    section.left_margin = Cm(2.5)
    section.right_margin = Cm(1.0)
    section.header_distance = Cm(0)
    section.footer_distance = Cm(0.7)


def run_font(run, size=14, bold=False, italic=False, family="Times New Roman") -> None:
    run.font.name = family
    run._element.rPr.rFonts.set(qn("w:eastAsia"), family)
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    run.font.color.rgb = RGBColor(0, 0, 0)


def configure_styles(doc: Document) -> None:
    for name in ["Normal", "Heading 1", "Heading 2", "Heading 3"]:
        style = doc.styles[name]
        style.font.name = "Times New Roman"
        style._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
        style.font.size = Pt(14)
        style.font.color.rgb = RGBColor(0, 0, 0)
        style.paragraph_format.line_spacing = 1.5
        style.paragraph_format.space_before = Pt(0)
        style.paragraph_format.space_after = Pt(0)
        if style._element.pPr is not None and style._element.pPr.numPr is not None:
            style._element.pPr.remove(style._element.pPr.numPr)
    doc.styles["Normal"].paragraph_format.first_line_indent = Cm(1.25)
    for name in ["Heading 1", "Heading 2", "Heading 3"]:
        doc.styles[name].font.bold = True
        doc.styles[name].paragraph_format.first_line_indent = Cm(0)


def add_complex_field(paragraph, instruction: str, placeholder: str = "") -> None:
    run = paragraph.add_run()
    fld_begin = OxmlElement("w:fldChar")
    fld_begin.set(qn("w:fldCharType"), "begin")
    run._r.append(fld_begin)

    instr_run = paragraph.add_run()
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = " " + instruction + " "
    instr_run._r.append(instr)

    sep_run = paragraph.add_run()
    fld_sep = OxmlElement("w:fldChar")
    fld_sep.set(qn("w:fldCharType"), "separate")
    sep_run._r.append(fld_sep)

    if placeholder:
        res = paragraph.add_run(placeholder)
        run_font(res)

    end_run = paragraph.add_run()
    fld_end = OxmlElement("w:fldChar")
    fld_end.set(qn("w:fldCharType"), "end")
    end_run._r.append(fld_end)


def set_update_fields(docx_path: Path) -> None:
    with zipfile.ZipFile(docx_path, "a") as zf:
        xml = zf.read("word/settings.xml").decode("utf-8")
        if "w:updateFields" not in xml:
            insert = '<w:updateFields w:val="true"/>'
            xml = xml.replace("</w:settings>", insert + "</w:settings>")
            zf.writestr("word/settings.xml", xml)


def add_page_numbering(doc: Document) -> None:
    for section in doc.sections:
        footer = section.footer
        par = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
        par.text = ""
        par.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        par.paragraph_format.first_line_indent = Cm(0)
        r = par.add_run("Арк. ")
        run_font(r, size=12)
        add_complex_field(par, "PAGE", "1")


def add_bookmark(paragraph, name: str) -> None:
    global bookmark_counter
    start = OxmlElement("w:bookmarkStart")
    start.set(qn("w:id"), str(bookmark_counter))
    start.set(qn("w:name"), name)
    end = OxmlElement("w:bookmarkEnd")
    end.set(qn("w:id"), str(bookmark_counter))
    paragraph._p.insert(0, start)
    paragraph._p.append(end)
    bookmark_counter += 1


def add_hyperlink(paragraph, text: str, anchor: str) -> None:
    hyperlink = OxmlElement("w:hyperlink")
    hyperlink.set(qn("w:anchor"), anchor)
    new_run = OxmlElement("w:r")
    r_pr = OxmlElement("w:rPr")
    color = OxmlElement("w:color")
    color.set(qn("w:val"), "000000")
    r_pr.append(color)
    new_run.append(r_pr)
    t = OxmlElement("w:t")
    t.text = text
    new_run.append(t)
    hyperlink.append(new_run)
    paragraph._p.append(hyperlink)


def p(doc: Document, text: str = "", *, first=True, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
      bold=False, italic=False, size=14):
    par = doc.add_paragraph()
    par.paragraph_format.first_line_indent = Cm(1.25 if first else 0)
    par.paragraph_format.line_spacing = 1.5
    par.paragraph_format.space_before = Pt(0)
    par.paragraph_format.space_after = Pt(0)
    par.alignment = align
    if text:
        r = par.add_run(text)
        run_font(r, size=size, bold=bold, italic=italic)
    return par


def center(doc: Document, text: str, size=14, bold=True):
    return p(doc, text, first=False, align=WD_ALIGN_PARAGRAPH.CENTER, bold=bold, size=size)


def page_break(doc: Document) -> None:
    doc.add_page_break()


def heading(doc: Document, text: str, bookmark: str, level=1, break_before=False):
    if break_before:
        page_break(doc)
    style = f"Heading {min(level, 3)}"
    par = doc.add_paragraph(style=style)
    par.paragraph_format.first_line_indent = Cm(0)
    par.paragraph_format.space_before = Pt(12 if level > 1 else 18)
    par.paragraph_format.space_after = Pt(6)
    par.paragraph_format.line_spacing = 1.5
    par.alignment = WD_ALIGN_PARAGRAPH.CENTER if level == 1 else WD_ALIGN_PARAGRAPH.LEFT
    r = par.add_run(text)
    run_font(r, bold=True)
    add_bookmark(par, bookmark)
    return par


def bullet(doc: Document, text: str) -> None:
    par = doc.add_paragraph(style=None)
    par.paragraph_format.left_indent = Cm(1.25)
    par.paragraph_format.first_line_indent = Cm(-0.5)
    par.paragraph_format.line_spacing = 1.5
    par.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    r = par.add_run("- " + text)
    run_font(r)


def set_cell_margins(cell, top=80, start=80, bottom=80, end=80) -> None:
    tc = cell._tc
    tc_pr = tc.get_or_add_tcPr()
    tc_mar = tc_pr.first_child_found_in("w:tcMar")
    if tc_mar is None:
        tc_mar = OxmlElement("w:tcMar")
        tc_pr.append(tc_mar)
    for m, v in [("top", top), ("start", start), ("bottom", bottom), ("end", end)]:
        node = tc_mar.find(qn(f"w:{m}"))
        if node is None:
            node = OxmlElement(f"w:{m}")
            tc_mar.append(node)
        node.set(qn("w:w"), str(v))
        node.set(qn("w:type"), "dxa")


def set_borders(table) -> None:
    tbl_pr = table._tbl.tblPr
    borders = tbl_pr.find(qn("w:tblBorders"))
    if borders is None:
        borders = OxmlElement("w:tblBorders")
        tbl_pr.append(borders)
    for edge in ["top", "left", "bottom", "right", "insideH", "insideV"]:
        el = borders.find(qn("w:" + edge))
        if el is None:
            el = OxmlElement("w:" + edge)
            borders.append(el)
        el.set(qn("w:val"), "single")
        el.set(qn("w:sz"), "6")
        el.set(qn("w:space"), "0")
        el.set(qn("w:color"), "000000")


def shade(cell, fill="D9EAF7") -> None:
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = tc_pr.find(qn("w:shd"))
    if shd is None:
        shd = OxmlElement("w:shd")
        tc_pr.append(shd)
    shd.set(qn("w:fill"), fill)


def cell_text(cell, text, bold=False, align=None) -> None:
    cell.text = ""
    par = cell.paragraphs[0]
    par.paragraph_format.first_line_indent = Cm(0)
    par.paragraph_format.line_spacing = 1.15
    par.paragraph_format.space_after = Pt(0)
    par.alignment = align or (WD_ALIGN_PARAGRAPH.CENTER if len(str(text)) < 24 else WD_ALIGN_PARAGRAPH.LEFT)
    r = par.add_run(str(text))
    run_font(r, size=11, bold=bold)
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER
    set_cell_margins(cell)


def table(doc: Document, caption: str, headers: list[str], rows: list[list[str]]) -> None:
    p(doc, caption, first=False, align=WD_ALIGN_PARAGRAPH.LEFT)
    t = doc.add_table(rows=1, cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.autofit = True
    set_borders(t)
    for i, h in enumerate(headers):
        cell_text(t.rows[0].cells[i], h, True)
        shade(t.rows[0].cells[i])
    for row in rows:
        cells = t.add_row().cells
        for i, value in enumerate(row):
            cell_text(cells[i], value)
    doc.add_paragraph()


def figure_placeholder(doc: Document, description: str, caption: str) -> None:
    p(doc, f"({description})", first=False, align=WD_ALIGN_PARAGRAPH.CENTER, italic=True)
    p(doc, caption, first=False, align=WD_ALIGN_PARAGRAPH.CENTER)


def toc_entry(doc: Document, title: str, bookmark: str, level=1) -> None:
    par = doc.add_paragraph()
    par.paragraph_format.first_line_indent = Cm(0)
    par.paragraph_format.left_indent = Cm((level - 1) * 0.55)
    par.paragraph_format.line_spacing = 1.5
    par.paragraph_format.tab_stops.add_tab_stop(Cm(16.5), alignment=2, leader=1)
    add_hyperlink(par, title, bookmark)
    par.add_run("\t")
    add_complex_field(par, f"PAGEREF {bookmark} \\h", "1")


def add_toc(doc: Document, items: list[tuple[str, str, int]]) -> None:
    heading(doc, "Зміст", "toc", level=1)
    for title, bookmark, level in items:
        toc_entry(doc, title, bookmark, level)


def add_code(doc: Document, path: str, title: str, max_lines=120) -> None:
    heading(doc, title, title.lower().replace(" ", "_").replace("–", "_")[:35], level=2, break_before=True)
    file_path = Path(path)
    if not file_path.exists():
        p(doc, f"Файл {path} не знайдено у робочій директорії.", first=False)
        return
    lines = file_path.read_text(encoding="utf-8", errors="ignore").splitlines()
    if len(lines) > max_lines:
        lines = lines[:max_lines] + ["// ... фрагмент скорочено для пояснювальної записки ..."]
    for line in lines:
        par = doc.add_paragraph()
        par.paragraph_format.first_line_indent = Cm(0)
        par.paragraph_format.line_spacing = 1.0
        par.paragraph_format.space_after = Pt(0)
        r = par.add_run(line[:130])
        run_font(r, size=8.5, family="Courier New")


